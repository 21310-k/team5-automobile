/****************************************************************************************************************/
/*	bios_beep_output関数																						*/
/****************************************************************************************************************/
void bios_beep_output(unsigned char A)
{
	if(A==1)
	{
		BEEP_PORT=BEEP_ON;
	}
	else if(A==0)
	{
		BEEP_PORT=BEEP_OFF;
	}
}